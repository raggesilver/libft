#include "../libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	char *p;

	p = dest;
	while (*p)
		p++;
	while (n > 0 && *src)
	{
		*p++ = *src++;
		n--;
	}
	*p = 0;
	return (dest);
}
